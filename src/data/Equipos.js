// Función para buscar equipos
export const buscarEquipos = (elegido) => {
  const elegidoLower = elegido.toLowerCase();
  return equiposDatabase.filter(equipo => 
    equipo.nombre.toLowerCase().includes(elegidoLower) ||
    equipo.nombreCorto.toLowerCase().includes(elegidoLower) ||
    equipo.liga.toLowerCase().includes(elegidoLower) ||
    equipo.ciudad.toLowerCase().includes(elegidoLower)
  );
};

// Función para obtener equipo por ID
export const obtenerEquipoPorId = (id) => {
  return equiposDatabase.find(equipo => equipo.id === id);
};

// Función para obtener equipos por liga
export const obtenerEquiposPorLiga = (liga) => {
  return equiposDatabase.filter(equipo => equipo.liga === liga);
};